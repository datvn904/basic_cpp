#include <bits/stdc++.h>
using namespace std;
unsigned int n, m;

int main() {

  cin >> n >> m;
  cout << n << " + " << m << " = " << n + m << endl;
  cout << n << " - " << m << " = " << n - m << endl;
  cout << n << " * " << m << " = " << n * m << endl;
  cout << n << " div " << m << " = " << n / m;
  return 0;
}

